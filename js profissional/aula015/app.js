//Estrutura de Loop For

/* Existem dois tipos de loops, os definidos e os indefinidos, ou seja, 
loops quando temos noção da quantidade de vezes que irei repetir e
loops onde não sei ao certo a quantidade de vezes que vai ocorrer a 
repetição, respectivamente.

Loops definidos ---> FOR
Loops indefinidos ---> While/ Do While */

console.log('Inicio do programa')

for(let i = 0; i < 10; i++){
    console.log(`CFB Cursos - valor de i: ${i}`)
}

console.log('Fim do programa')
 
/* Assim como é possível perceber no método acima, 
a estrutura de loop definido for é separada em três
partes. 'for(inicialização ; condição de execução ; controle)'*/